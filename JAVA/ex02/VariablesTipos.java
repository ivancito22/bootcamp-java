
package ex02;

public class VariablesTipos {
  public static void main(String[] args) {
      int numeroEntero = 42;
      double numeroDecimal = 3.14d;
      boolean esVerdadero = true;
      char caracter = 'A';
      String cadenaTexto = "Hola, mundo";

      System.out.println("Valor del entero: " + numeroEntero);
      System.out.println("Valor del decimal: " + numeroDecimal);
      System.out.println("Valor del booleano: " + esVerdadero);
      System.out.println("Valor del caracter: " + caracter);
      System.out.println("Valor de la cadena de texto: " + cadenaTexto);
  }
}